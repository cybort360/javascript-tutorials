// strings

const myVariable = "Mathematics"

// the length property

console.log(myVariable.length);

//string method

console.log(myVariable.charAt(5)); //position of the string

console.log(myVariable.indexOf("Mat")); //first occurence of the word

console.log(myVariable.lastIndexOf("at")); //last occurence of the word

console.log(myVariable.slice(5, 6)); //accepts starting and ending parameters

console.log(myVariable.toUpperCase()) //doesnt require any parameter

console.log(myVariable.toLowerCase());

console.log(myVariable.includes("mat")); //accept boolean data

console.log(myVariable.split("")); //splits the screen where ever the character we provide occur